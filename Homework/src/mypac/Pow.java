package mypac;

public class Pow {
    public static void main(String[] args) {
        int a = -20;
        int b = 92;
        int c = 638;
        int d = 73;
        int e = 200;
        int f = -22;


        System.out.println("The square root of -20 is: " + (int)Math.pow(a,2));
        System.out.println("The square root of 92 is: " + (int)Math.pow(b,2));
        System.out.println("The square root of 638 is: " + (int)Math.pow(c,2));
        System.out.println("The square root of 73 is: " + (int)Math.pow(d,2));
        System.out.println("The square root of 200 is: " + (int)Math.pow(e,2));
        System.out.println("The square root of -22 is: " + (int)Math.pow(f,2));

    }
}
